export const MESSAGES: Record<Lang, Dict> = { en, 'pt-br': ptBr, es, fr, de, it, nl, ru, pl };

/** Translate a key; falls back to English, then to the key itself. Interpolates {vars}. */
export function t(lang: Lang, key: string, vars?: Record<string, string>): string {
  const msg = MESSAGES[lang]?.[key] ?? MESSAGES.en[key] ?? key;
  if (!vars) return msg;
  return msg.replace(/\{(\w+)\}/g, (_, k: string) => vars[k] ?? `{${k}}`);
}

/** Human label for a Harper category; falls back to the raw category name. */
export function categoryLabel(lang: Lang, category: string): string {
  const key = `category.${category}`;
  const msg = MESSAGES[lang]?.[key] ?? MESSAGES.en[key];
  return msg ?? category;
}

/** English name of each language — for AI prompts (translate target, define language). */
export const LANG_NAME: Record<Lang, string> = {
  en: 'English', 'pt-br': 'Portuguese', es: 'Spanish', fr: 'French', de: 'German',
  it: 'Italian', nl: 'Dutch', ru: 'Russian', pl: 'Polish',
};

/** dictionaryapi.dev language code for a UI language (pt-br needs the pt-BR form). */
export function dictCode(lang: Lang): string {
  return lang === 'pt-br' ? 'pt-BR' : lang;
}

/** Map a BCP-47 locale to a supported UI language (falls back to English). */
export function detectLang(locale: string): Lang {
  const l = locale.toLowerCase();
  if (l.startsWith('pt')) return 'pt-br';
  if (l.startsWith('es')) return 'es';
  if (l.startsWith('fr')) return 'fr';
  if (l.startsWith('de')) return 'de';
  if (l.startsWith('it')) return 'it';
  if (l.startsWith('nl')) return 'nl';
  if (l.startsWith('ru')) return 'ru';
  if (l.startsWith('pl')) return 'pl';
  return 'en';
}
